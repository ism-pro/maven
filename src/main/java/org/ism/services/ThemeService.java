    public List<Theme> getThemes() {
        return themes;
    }

    public Theme getStrTheme(String name) {
        if (themes == null) {
            init();
        }
        if (name == null) {
            return themes.get(7);
        }
        if (name.trim().isEmpty()) {
            return themes.get(7);
        }

        Iterator<Theme> it = this.themes.iterator();
        while (it.hasNext()) {
            Theme t = it.next();
            if (t.getName().matches(name)) {
                return t;
            }
        }
        return themes.get(7);
    }

    public Theme getTheme(java.lang.Integer id) {
        return themes.get(id);
    }

    @FacesConverter(forClass = Theme.class)
    public static class ThemeServiceConverter implements Converter {

        @Override
        public Object getAsObject(FacesContext facesContext, UIComponent component, String value) {
            if (value == null || value.length() == 0) {
                return null;
            }
            ThemeService service = (ThemeService) facesContext.getApplication().getELResolver().
                    getValue(facesContext.getELContext(), null, "themeService");
            return service.getTheme(getKey(value));
        }

        java.lang.Integer getKey(String value) {
            java.lang.Integer key;
            key = Integer.valueOf(value);
            return key;
        }

        String getStringKey(java.lang.Integer value) {
            StringBuilder sb = new StringBuilder();
            sb.append(value);
            return sb.toString();
        }

        @Override
        public String getAsString(FacesContext facesContext, UIComponent component, Object object) {
            if (object == null) {
                return null;
            }
            if (object instanceof Theme) {
                Theme o = (Theme) object;
                return getStringKey(o.getId());
            } else {
                Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, "object {0} is of type {1}; expected type: {2}", new Object[]{object, object.getClass().getName(), Theme.class.getName()});
                return null;
            }
        }

    }
